<h1>Proyecto MythRA</h1>
<br>
<p class="letra_de_parrafo justificar_texto">
	El proyecto mythra consiste en un videojuego de realidad aumentada para Smartphones con sistema operativo Android,
	el juego fue construido en C# sobre el motor Gráfico Unity en el año 2014, y se han lanzado tres versiones, agregando nuevas 
	funcionalidades y correciones varias.

	Este videojuego se presento en la feria de innovación de Duoc UC, obteniendo el cuarto lugar de entre 28 proyectos.
	la última versión de este juego fue lanzada en enero de este año, agilizando el videojuego, debido a que se renovo
	la arquitectura interna de código,dando paso a un juego mucho mas fluido y que hace mayor uso de la Realidad aumentada.
